//To print DoobeeDoobeeDo using while and if
public class Doobee {
    public static void main(String [] args)
    {
        int x=1;
        while(x<3)
        {
            System.out.print("Doobee");
            x=x+1;
        }
        if(x==3)
        {
            System.out.print("Do");
        }
    }
}
